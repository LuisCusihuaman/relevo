-- Tabla HANDOVER_CHECKLISTS (listas de verificación para síntesis)
CREATE TABLE HANDOVER_CHECKLISTS (
    ID VARCHAR2(50) PRIMARY KEY,
    HANDOVER_ID VARCHAR2(50) NOT NULL,
    USER_ID VARCHAR2(255) NOT NULL,
