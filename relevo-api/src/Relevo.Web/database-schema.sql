    CONSTRAINT FK_CHECKLIST_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT UK_CHECKLIST_ITEM UNIQUE (HANDOVER_ID, USER_ID, ITEM_ID)
